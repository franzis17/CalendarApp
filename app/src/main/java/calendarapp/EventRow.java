package calendarapp.app;

public class EventRow
{
    private String title;
    
    public EventRow(String title)
    {
        this.title = title;
    }
    
    
    /* [GETTERS] */
    
    public String getTitle()
    {
        return title;
    }
    
    
    /* [SETTERS] */
    
    public void setTitle(String title)
    {
        this.title = title;
    }
}
